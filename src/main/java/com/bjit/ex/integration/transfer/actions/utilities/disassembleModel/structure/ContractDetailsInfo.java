package com.bjit.ex.integration.transfer.actions.utilities.disassembleModel.structure;

/**
 *
 * @author Sajjad
 */
public class ContractDetailsInfo {
    private String project;
    private String activity;
    private String milestrone;
    private String contract;
    private String contractLine;
    private String deliverableNumber;
    private String deliverableItem;
    private String orderQty;

    public String getProject() {
        return project;
    }

    public void setProject(String project) {
        this.project = project;
    }

    public String getActivity() {
        return activity;
    }

    public void setActivity(String activity) {
        this.activity = activity;
    }

    public String getMilestrone() {
        return milestrone;
    }

    public void setMilestrone(String milestrone) {
        this.milestrone = milestrone;
    }

    public String getContract() {
        return contract;
    }

    public void setContract(String contract) {
        this.contract = contract;
    }

    public String getContractLine() {
        return contractLine;
    }

    public void setContractLine(String contractLine) {
        this.contractLine = contractLine;
    }

    public String getDeliverableNumber() {
        return deliverableNumber;
    }

    public void setDeliverableNumber(String deliverableNumber) {
        this.deliverableNumber = deliverableNumber;
    }

    public String getDeliverableItem() {
        return deliverableItem;
    }

    public void setDeliverableItem(String deliverableItem) {
        this.deliverableItem = deliverableItem;
    }

    public String getOrderQty() {
        return orderQty;
    }

    public void setOrderQty(String orderQty) {
        this.orderQty = orderQty;
    }
}
